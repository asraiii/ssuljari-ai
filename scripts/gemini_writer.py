import os
import google.generativeai as genai

genai.configure(api_key=os.environ["GEMINI_API_KEY"])
model = genai.GenerativeModel("gemini-2.5-flash")


def generate_content_pack(title, content):

    prompt = f"""
너는 한국 유튜브 쇼츠 채널 '썰자리'의 콘텐츠 제작 AI다.

아래 Reddit 글을 기반으로 조회수가 가장 잘 나올 한국 쇼츠 콘텐츠를 만든다.

━━━━━━━━━━━━━━
[Reddit 제목]
{title}

[Reddit 내용]
{content}
━━━━━━━━━━━━━━

목표

- 미국 글을 한국인이 실제 겪은 일처럼 완전히 재구성한다.
- 번역처럼 보이면 실패다.
- 블라인드, 네이트판, 에브리타임에 올라올 것 같은 현실적인 썰을 만든다.

========================

반드시 수행할 작업

① 쇼츠 대본 작성

- 45~60초
- 한국식
- 1인칭
- 첫 문장은 강한 Hook
- 마지막은 반전 또는 여운
- 등장인물 최대 3명

- 반드시 줄바꿈으로 작성한다.
- 한 줄에는 한 문장만 작성한다.
- 빈 줄은 넣지 않는다.

② 제목 생성

내부적으로 최소 10개의 제목을 만든다.

③ 썸네일 생성

내부적으로 최소 5개의 썸네일 문구를 만든다.

④ Hook 생성

내부적으로 최소 5개의 Hook을 만든다.

⑤ 내부 평가

다음 기준으로 스스로 평가한다.

- CTR
- 클릭률
- 감정 자극
- 궁금증
- 한국인 공감
- 쇼츠 중독성
- 블라인드 감성
- 네이트판 감성

⑥ 최종 선택

위 후보들을 비교하여

가장 좋은 제목 1개

가장 좋은 썸네일 1개

가장 좋은 Hook 1개

만 출력한다.

후보들은 절대 출력하지 않는다.

========================

추가 규칙

- 미국 문화 제거
- 미국 회사 제거
- 미국 학교 제거
- 미국 법률 제거
- 미국 지명 제거

- 한국 직장
- 한국 대학
- 한국 연애
- 한국 결혼
- 한국 가족 문화로 변경

- 설명 금지
- 영상 연출 금지
- 괄호 금지
- 번역체 금지
- thumbnail에는 괄호를 절대 사용하지 마라.
- thumbnail에는 사람 표정이나 이미지 설명을 넣지 마라.
- thumbnail은 3~8글자의 짧은 문구만 작성한다.

========================

반드시 JSON 하나만 출력한다.

JSON 외에는 어떤 문자도 출력하지 않는다.

출력 형식

{{
    "story":"쇼츠 대본",

    "title":"최종 제목",

    "thumbnail":"최종 썸네일",

    "hook":"최종 Hook",

    "hashtags":[
        "#썰",
        "#연애",
        "#실화",
        "#쇼츠",
        "#썰자리"
    ]
}}

규칙

story는 문자열이다.

story 안에는 줄바꿈(\n)을 포함한다.

한 줄은 반드시 한 문장이다.

title은 문자열 하나이다.

thumbnail은 문자열 하나이다.

hook은 문자열 하나이다.

hashtags는 문자열 배열이다.

내부적으로 여러 후보를 생성하고 비교하더라도

JSON에는 최종 결과 하나만 출력한다.

JSON 형식이 아니면 실패다.

다른 문장은 절대 출력하지 마라.
"""

    response = model.generate_content(prompt)
    return response.text
