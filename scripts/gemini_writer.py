import os
import google.generativeai as genai

genai.configure(api_key=os.environ["GEMINI_API_KEY"])
model = genai.GenerativeModel("gemini-2.5-flash")


def generate_content_pack(title, content):

    prompt = f"""
너는 한국 유튜브 쇼츠 채널 '썰자리'의 콘텐츠 제작 AI다.

아래 Reddit 글을 기반으로 조회수가 잘 나올 한국 쇼츠 콘텐츠 패키지를 만들어라.

━━━━━━━━━━━━━━
[Reddit 제목]
{title}

[Reddit 내용]
{content}
━━━━━━━━━━━━━━

목표
- 미국 글을 한국인이 실제 겪은 일처럼 완전히 재구성한다.
- 번역처럼 보이면 실패다.
- 블라인드, 네이트판, 에브리타임에 올라올 것 같은 현실적인 썰을 만든다.

반드시 생성할 것

1. 한국식 쇼츠 썰
- 45~60초 분량
- 완전히 한국화
- 1인칭
- 한 줄 = 한 문장
- 첫 문장은 반드시 강한 훅
- 마지막은 여운 또는 반전
- 등장인물 최대 3명

2. 조회수용 제목 10개 생성

3. 썸네일 문구 5개 생성

4. 첫 문장(Hook) 5개 생성

5. AI가 직접 평가한다.

평가기준

- 클릭률
- 궁금증
- 감정 자극
- 한국인 공감
- 쇼츠 중독성
- 블라인드 감성
- 네이트판 감성

6. 위 후보들을 비교한 뒤

가장 조회수가 높을 제목 1개

가장 조회수가 높을 썸네일 1개

가장 조회수가 높을 Hook 1개

만 최종 선택하여 출력한다.

절대로 첫 번째 후보를 그대로 사용하지 마라.

추가 규칙

- 미국 문화 제거
- 미국 회사 제거
- 미국 학교 제거
- 미국 법률 제거
- 미국 지명 제거

- 한국 직장
- 한국 대학
- 한국 연애
- 한국 결혼
- 한국 가족 문화로 변경

- 설명 금지
- 영상 연출 금지
- 괄호 금지
- 번역체 금지

━━━━━━━━━━━━━━

반드시 JSON 하나만 출력한다.

설명 금지
마크다운 금지
```json 금지
JSON 외의 문자 출력 금지

출력 형식은 반드시 아래와 같다.

{{
  "story": "쇼츠 대본",

  "titles": [
    "제목1",
    "제목2",
    "제목3",
    "제목4",
    "제목5"
  ],

  "thumbnail": "썸네일 문구",

  "hook": "첫 문장",

  "hashtags": [
    "#썰",
    "#연애",
    "#실화",
    "#쇼츠",
    "#썰자리"
  ]
}}

규칙

story는 문자열 하나이다.

title은 문자열 하나이다.

thumbnail은 문자열 하나이다.

hook은 문자열 하나이다.

hashtags는 문자열 배열이다.

JSON 형식이 아니면 실패다.

출력은 반드시 위 JSON만 출력한다.

다른 문장은 절대 출력하지 마라.
"""

    response = model.generate_content(prompt)
    return response.text
