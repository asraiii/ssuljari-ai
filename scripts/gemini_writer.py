import os
import google.generativeai as genai

genai.configure(api_key=os.environ["GEMINI_API_KEY"])
model = genai.GenerativeModel("gemini-2.5-flash")


def generate_content_pack(title, content):

    prompt = f"""
너는 한국 유튜브 쇼츠 채널 '썰자리'의 콘텐츠 제작 AI다.

아래 Reddit 글을 기반으로 조회수가 가장 잘 나올 한국 쇼츠 콘텐츠를 만든다.

━━━━━━━━━━━━━━
[Reddit 제목]
{title}

[Reddit 내용]
{content}
━━━━━━━━━━━━━━

목표

- 미국 글을 한국인이 실제 겪은 일처럼 완전히 재구성한다.
- 번역처럼 보이면 실패다.
- 블라인드, 네이트판, 에브리타임에 올라올 것 같은 현실적인 썰을 만든다.

========================

반드시 수행할 작업

① 쇼츠 대본 작성

- 45~60초
- 한국식
- 1인칭
- 첫 문장은 강한 Hook
- 마지막은 반전 또는 여운
- 등장인물 최대 3명

- 반드시 줄바꿈으로 작성한다.
- 한 줄에는 한 문장만 작성한다.
- 한 문장은 반드시 25자 이하로 작성한다.
- 긴 문장은 둘 이상으로 나눈다.
- 빈 줄은 넣지 않는다.

② 제목 생성

내부적으로 최소 10개의 제목을 만든다.

제목 규칙

기사 제목처럼 쓰지 마라.

유튜브 쇼츠 스타일로 작성한다.

감정을 먼저 보여라.

충격적인 사건을 먼저 보여라.

20자 이내를 권장한다.

예시

남친이 제 통장 봤습니다

결혼식 전날 파혼당했어요

시어머니가 제 월급 가져갔습니다

친구가 제 남친이랑 잤습니다

③ 썸네일 생성

내부적으로 최소 5개의 썸네일 문구를 만든다.

썸네일 규칙

3~6글자

명사만 쓰지 마라.

감정을 담아라.

궁금증을 만든다.

예시

파혼 직전

소름 돋았다

결국 들켰다

진짜 미쳤네

이게 맞아?

④ Hook 생성

내부적으로 최소 5개의 Hook을 만든다.

첫 문장을 듣고 바로 끝까지 보고 싶어지는 수준이어야 한다.

Hook는 절대로 인사나 설명으로 시작하지 않는다.

첫 문장부터

사건이 이미 터진 상태에서 시작한다.

예시
남친이 제 통장을 몰래 봤습니다.

결혼식 전날 파혼당했습니다.

회사 단톡방에서 제 욕을 봤습니다.

친구가 제 남친이랑 바람났습니다.

독자가 이유를 궁금해서 끝까지 보게 만들어라.

첫 문장은 반드시 사건으로 시작한다.

감정 설명부터 시작하지 마라.

배경 설명부터 시작하지 마라.

⑤ 내부 평가

다음 기준으로 스스로 평가한다.

- CTR
- 클릭률
- 감정 자극
- 궁금증
- 한국인 공감
- 쇼츠 중독성
- 블라인드 감성
- 네이트판 감성

⑥ 최종 선택

위 후보들을 비교하여

가장 좋은 제목 1개

가장 좋은 썸네일 1개

가장 좋은 Hook 1개

만 출력한다.

후보들은 절대 출력하지 않는다.

선택 기준

제목은 조회수(CTR)가 가장 높을 것.

썸네일은 유튜브에서 가장 클릭하고 싶은 문구일 것.

Hook은 첫 문장만 듣고도 끝까지 보게 만드는 문장일 것.

평범하거나 설명형이면 탈락이다.

기사 제목처럼 쓰지 마라.

감정을 먼저 보여라.

갈등을 먼저 보여라.

궁금증을 남겨라.

쇼츠 알고리즘이 가장 좋아할 결과 하나만 출력한다.

조회수가 낮을 것 같은 평범한 결과는 모두 폐기한다.

가장 자극적이면서 현실적인 결과 하나만 출력한다.

결과가 안전하지만 평범하다면 탈락이다.

독자가 영상을 클릭하지 않을 것 같으면 다시 만든다.

최종 결과는 반드시 가장 클릭하고 싶은 하나여야 한다.

========================

추가 규칙

- 미국 문화 제거
- 미국 회사 제거
- 미국 학교 제거
- 미국 법률 제거
- 미국 지명 제거

- 한국 직장
- 한국 대학
- 한국 연애
- 한국 결혼
- 한국 가족 문화로 변경

- 설명 금지
- 영상 연출 금지
- 괄호 금지
- 번역체 금지
- thumbnail에는 괄호를 절대 사용하지 마라.
- thumbnail에는 사람 표정이나 이미지 설명을 넣지 마라.
- thumbnail은 3~8글자의 짧은 문구만 작성한다.

가장 중요한 규칙

Reddit 글은 아이디어의 출발점으로만 사용한다.

원문을 그대로 번역하거나 그대로 재구성하지 마라.

핵심 갈등만 유지하고 나머지는 자유롭게 새로 만든다.

등장인물을 바꿔도 된다.

상황을 바꿔도 된다.

직업을 바꿔도 된다.

결말을 바꿔도 된다.

반전을 새로 만들어도 된다.

조회수가 더 잘 나온다면 원문과 달라도 된다.

최종 결과는

블라인드

네이트판

에브리타임

디시인사이드

중 한 곳에 실제 올라온 글처럼 자연스러워야 한다.

독자는

"이거 진짜 있었던 일 아니야?"

라고 느껴야 한다.

현실성이 가장 중요하다.

단,

현실적이면서도

감정이 크게 흔들리는 사건을 우선한다.

독자가

"미쳤다..."

"와 진짜?"

"나라면 헤어진다"

라고 반응할 만한 갈등을 만든다.

평범한 연애 일상은 피한다.

한국 쇼츠에서 자주 사용하는 전개를 우선한다.

초반 3문장 안에 반드시 갈등을 만든다.

중반에는 갈등이 커져야 한다.

마지막에는 반전 또는 강한 여운으로 끝낸다.

========================

반드시 JSON 하나만 출력한다.

JSON 외에는 어떤 문자도 출력하지 않는다.

출력 형식

{{
    "story":"쇼츠 대본",

    "title":"최종 제목",

    "thumbnail":"최종 썸네일",

    "hook":"최종 Hook",

    "hashtags":[
        "#썰",
        "#연애",
        "#실화",
        "#쇼츠",
        "#썰자리"
    ]
}}

규칙

story는 문자열이다.

story 안에는 줄바꿈(\n)을 포함한다.

한 줄은 반드시 한 문장이다.

title은 문자열 하나이다.

thumbnail은 문자열 하나이다.

hook은 문자열 하나이다.

hashtags는 문자열 배열이다.

내부적으로 여러 후보를 생성하고 비교하더라도

JSON에는 최종 결과 하나만 출력한다.

JSON 형식이 아니면 실패다.

다른 문장은 절대 출력하지 마라.
"""

    response = model.generate_content(prompt)
    return response.text
