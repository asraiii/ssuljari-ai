import os
import google.generativeai as genai

genai.configure(api_key=os.environ["GEMINI_API_KEY"])
model = genai.GenerativeModel("gemini-2.5-flash")


def generate_content_pack(title, content):

    prompt = f"""
너는 한국 유튜브 쇼츠 채널 '썰자리'의 콘텐츠 제작 AI다.

아래 Reddit 글을 소재로 조회수가 가장 잘 나올 한국 쇼츠 콘텐츠를 만든다.

━━━━━━━━━━━━━━
[Reddit 제목]
{title}

[Reddit 내용]
{content}
━━━━━━━━━━━━━━

========================
목표
========================

- Reddit 글은 아이디어의 출발점으로만 사용한다.
- 미국 글을 한국인이 실제 겪은 일처럼 완전히 재창작한다.
- 번역처럼 보이면 실패다.
- 블라인드, 네이트판, 에브리타임, 디시인사이드에 실제 올라온 글처럼 자연스러워야 한다.
- 핵심 갈등만 유지하고 나머지는 자유롭게 변경한다.

변경 가능

- 등장인물
- 직업
- 장소
- 관계
- 대사
- 사건
- 결말
- 반전

조회수가 더 잘 나온다면 원문과 달라도 된다.

========================
① 쇼츠 대본
========================

- 45~60초
- 약 350~500자
- 1인칭
- 한국식 표현
- 등장인물 최대 3명

반드시

- 첫 문장은 Hook
- 첫 문장부터 사건이 터진 상태로 시작
- 배경 설명부터 시작하지 않는다.
- 감정 설명부터 시작하지 않는다.

전개

- 초반 3문장 안에 갈등 발생
- 중반 갈등 확대
- 마지막 반전 또는 여운

작성 규칙

- 줄바꿈 사용
- 한 줄 = 한 문장
- 한 문장 25자 이하
- 긴 문장은 나눈다.
- 빈 줄 금지

========================
② 제목
========================

내부적으로 10개 이상 만든 뒤 가장 좋은 1개만 출력한다.

규칙

- 유튜브 쇼츠 스타일
- 기사 제목 금지
- 감정과 사건을 먼저 보여준다.
- 궁금증을 남긴다.
- 20자 이내 권장

예시

남친이 제 통장 봤습니다
결혼식 전날 파혼당했어요
친구가 제 남친이랑 잤습니다

========================
③ 썸네일
========================

내부적으로 5개 이상 만든 뒤 가장 좋은 1개만 출력한다.

규칙

- 3~8글자
- 괄호 금지
- 사람 표정 설명 금지
- 명사만 쓰지 않는다.
- 감정과 궁금증을 만든다.

예시

파혼 직전
결국 들켰다
이게 맞아
진짜 미쳤네

========================
④ Hook
========================

내부적으로 5개 이상 만든 뒤 가장 좋은 1개만 출력한다.

규칙

- 첫 문장만 듣고 끝까지 보게 만든다.
- 사건부터 시작한다.
- 설명 금지
- 인사 금지
- 감정 설명 금지최종
- Hook은 story의 첫 번째 문장과 반드시 동일해야 한다.

예시

남친이 제 통장을 몰래 봤습니다.
회사 단톡방에서 제 욕을 봤습니다.
친구가 제 남친이랑 바람났습니다.

========================
⑤ 최종 선택
========================

후보를 스스로 평가한다.

평가 기준

- CTR
- 클릭률
- 감정 자극
- 궁금증
- 한국인 공감
- 쇼츠 중독성

조회수가 낮을 것 같은 결과는 모두 폐기한다.

평범하면 다시 만든다.

가장 클릭하고 싶은 결과 하나만 출력한다.

독자가

"미쳤다"

"와 진짜?"

"나라면 헤어진다"

라고 반응할 수준이어야 한다.

========================
추가 규칙
========================

반드시 한국 문화로 변경한다.

삭제

- 미국 회사
- 미국 학교
- 미국 지명
- 미국 법률
- 미국 문화

변경

- 한국 직장
- 한국 대학
- 한국 가족 문화
- 한국 연애
- 한국 결혼

설명체 금지

번역체 금지

영상 연출 금지

========================
출력
========================

반드시 JSON 하나만 출력한다.

절대로 ```json 코드블록을 사용하지 마라.

반드시 순수 JSON만 출력한다.

{{
    "story":"...",
    "title":"...",
    "thumbnail":"...",
    "hook":"...",
    "hashtags":[
        "#썰",
        "#연애",
        "#실화",
        "#쇼츠",
        "#썰자리"
    ]
}}

규칙

story는 문자열이다.

story 안에는 줄바꿈(\n)을 포함한다.

story의 첫 문장은 반드시 가장 자극적인 문장이어야 한다.

한 줄은 반드시 한 문장이다.

title은 문자열 하나이다.

thumbnail은 문자열 하나이다.

hook은 문자열 하나이다.

hashtags는 문자열 배열이다.

내부적으로 여러 후보를 생성하고 비교하더라도

JSON에는 최종 결과 하나만 출력한다.

JSON 형식이 아니면 실패다.

다른 문장은 절대 출력하지 마라.
"""

    response = model.generate_content(prompt)

    text = response.text.strip()

    # ```json 제거
    if text.startswith("```"):
        text = text.replace("```json", "")
        text = text.replace("```", "")
        text = text.strip()

    # JSON 부분만 추출
    start = text.find("{")
    end = text.rfind("}") + 1

    if start != -1 and end > start:
        text = text[start:end]

    return text
